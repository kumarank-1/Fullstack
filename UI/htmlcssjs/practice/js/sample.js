console.log("Welcome to JS Session");

// Create variables using var, let, and const
let x = 10;
var y = 20;
const z = 30;

// Print x, y, z
console.log("x is " + x);
console.log("y is " + y);
console.log("z is " + z);

// Check datatype of x, y, z
console.log("datatype of x:", typeof x);
console.log("datatype of y:", typeof y);
console.log("datatype of z:", typeof z);

// Change y to decimal and create a string variable for z concept demo
y = 20.22;
let zString = "George"; // using new variable because const z cannot be reassigned

console.log("After changes:");
console.log("y is " + y);
console.log("zString is " + zString);
console.log("datatype of y:", typeof y);
console.log("datatype of zString:", typeof zString);
